package com.photon.legacyhealth.pojo;

public class NeighborhoodData {
    String status;
    String statusCode;
    NeighborhoodDataResponse responseData;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(String statusCode) {
        this.statusCode = statusCode;
    }

    public NeighborhoodDataResponse getResponseData() {
        return responseData;
    }

    public void setResponseData(NeighborhoodDataResponse responseData) {
        this.responseData = responseData;
    }
}
