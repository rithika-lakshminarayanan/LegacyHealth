package com.photon.legacyhealth;

public class InfoWindowData {
    private String imgUrl;
    private String symptomName;
    private String tipHeading;
    private String tipBody;

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getSymptomName() {
        return symptomName;
    }

    public void setSymptomName(String symptomName) {
        this.symptomName = symptomName;
    }

    public String getTipHeading() {
        return tipHeading;
    }

    public void setTipHeading(String tipHeading) {
        this.tipHeading = tipHeading;
    }

    public String getTipBody() {
        return tipBody;
    }

    public void setTipBody(String tipBody) {
        this.tipBody = tipBody;
    }
}
